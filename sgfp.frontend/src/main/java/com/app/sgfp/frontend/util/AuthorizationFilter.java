package com.app.sgfp.frontend.util;

import javax.servlet.*;
import javax.servlet.annotation.WebFilter;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;
import java.io.IOException;

@WebFilter(filterName = "AuthFilter", urlPatterns = {"*.xhtml"})
public class AuthorizationFilter implements Filter {

    public AuthorizationFilter() {
    }

    @Override
    public void init(FilterConfig filterConfig) throws ServletException {

    }

    @Override
    public void doFilter(ServletRequest request, ServletResponse response,
            FilterChain chain) throws IOException, ServletException {
        try {

            HttpServletRequest reqt = (HttpServletRequest) request;
            HttpServletResponse resp = (HttpServletResponse) response;
            HttpSession ses = reqt.getSession(false);
            String reqURI = reqt.getRequestURI();
            if ((reqURI.indexOf("/app/login") >= 0) && (ses != null && ses.getAttribute("username") != null)) {
                        resp.sendRedirect(reqt.getContextPath() + "/app/content");
            } else if (reqURI.indexOf("/app/login") >= 0
                    /*|| reqURI.indexOf("/app/usuario/recuperar.xhtml") >= 0*/
                    || (ses != null && ses.getAttribute("username") != null)
                    || reqURI.indexOf("/public/") >= 0
                    || reqURI.contains("javax.faces.resource")) {
                chain.doFilter(request, response);
            } else {
                resp.sendRedirect(reqt.getContextPath() + "/app/login");
            }

        } catch (Exception e) {

        }
    }

    @Override
    public void destroy() {

    }
}
